class Dowhileloopstatement{
    public static void main(String[] args) {
        int n=1;// this n = 1 is (initialization) is the starting point of the loop.
        do{
            System.out.print(n+"\n");
            ++n;
        }
        while(n<=10);  // this is n<=10 is a ending point of the loop.
    }
}