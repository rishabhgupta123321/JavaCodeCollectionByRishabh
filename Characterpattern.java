    public static void main(){
        char count = 'A';
        for(char i = 'A'; i<= 'E'; i++){     // here we have to see the iteration that when any condition will true then this loop will iterate
            for(char j = 'A'; j<=i; j++){
                System.out.print(count+" ");
                count++;
            }
            System.out.println();
        } 
    }
