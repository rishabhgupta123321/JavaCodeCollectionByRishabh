import java.util.Scanner;
/**
 * Relationaloperator
 */
public class Relationaloperator {
public static void main(String []args)
{
    int a, b;
    Scanner obj = new Scanner(System.in);
    System.out.print("Enter any two numbers.... ");
    a = obj.nextInt();
    b = obj.nextInt();
    System.out.println("true/false- "+(a>b));
    System.out.println("true/false- "+(a<b));
    System.out.println("true/false- "+(a>=b));
    System.out.println("true/false- "+(a<=b));
    System.out.println("true/false- "+(a!=b));
    System.out.println("true/false- "+(a==b));
    obj.close();
}
}
















